# what is http 
"The Hypertext Transfer Protocol (HTTP) "
"is designed to enable communications between clients and servers"
"HTTP works as a request-response protocol between a client and server"
"Example: A client (browser) sends an HTTP request to the server"
"then the server returns a response to the client"
# what is https
"HTTPS stands for hypertext transfer protocol secure and is the encrypted version of HTTP"
"It is used for secure communication across the internet or a network"
"The communication protocol is encrypted using Transport Layer Security (TLS) or formerly Secure Sockets Layer (SSL)"


